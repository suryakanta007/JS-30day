//push() method.
let arr = [5, 6, 7, 8, 9, 23, 5];
arr.push(10);
console.log(arr);

//pop() method.
let result = arr.pop(); //give the last element of the array.
console.log(result);

//shift() method.
let res = arr.shift(); //give the 1st element of the array.
console.log(res);

//unShift() method.
arr.unshift(7); //add a element on the 0th index of the array.
console.log(arr);
