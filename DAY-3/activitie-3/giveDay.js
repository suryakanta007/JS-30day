let date = 5;
switch (date) {
    case 1:{
        console.log("Mon day");
        break;
    }
    case 2:{
        console.log("Thes Day");
        break;
    }
    case 3:{
        console.log("Wednes Day");
        break;
    }
    case 4:{
        console.log("Thurs Day");
        break;
    }
    case 5:{
        console.log("Fri Day");
        break;
    }
    case 6:{
        console.log("Satur Day");
        break;
    }case 7:{
        console.log("Sun Day");
        break;
    }
    default:{
        console.log("Please enter a date in between 1 to 7");
    }
}